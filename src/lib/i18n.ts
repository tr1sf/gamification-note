// Reactive language signal so consumers of `t()` re-render when the language
// changes — historically the app forced `window.location.reload()` to work
// around `t()` being non-reactive. Reading this signal inside `t()` makes
// every consumer reactive.
const [langSignal, setLangSignal] = createSignal<"en" | "vi">(getCurrentLang());

export function t(key: string): string {
  // Reading the signal inside t() registers a reactive dependency for any
  // consumer rendered inside a SolidJS tracking scope.
  const lang = langSignal();
  if (lang === "vi" && vi[key]) return vi[key];
  return key;
}

export function getCurrentLang(): "en" | "vi" {
  if (typeof localStorage === "undefined") return "en";
  return (localStorage.getItem("lang") as "en" | "vi") || "en";
}

export function applyLanguage(lang: "en" | "vi"): void {
  if (typeof localStorage !== "undefined") {
    localStorage.setItem("lang", lang);
  }
  if (typeof document !== "undefined") {
    document.documentElement.setAttribute("lang", lang);
  }
  // Push the new language into the signal — every `t()` consumer re-renders
  // automatically, so toggles no longer need `window.location.reload()`.
  setLangSignal(lang);
}

export function getLangLabel(lang: "en" | "vi"): string {
  return lang === "en" ? "English" : "Tiếng Việt";
}
